package com.company;

public class People {
    private String namePer;
    private int agePer;
    private String hobbyPer;

    public String getNamePer() {
        return namePer;
    }

    public void setNamePer(String namePer) {
        this.namePer = namePer;
    }

    public int getAgePer() {
        return agePer;
    }

    public void setAgePer(int agePer) {
        this.agePer = agePer;
    }

    public String getHobbyPer() {
        return hobbyPer;
    }

    public void setHobbyPer(String hobbyPer) {
        this.hobbyPer = hobbyPer;
    }
}